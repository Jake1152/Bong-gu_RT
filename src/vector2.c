#include "vector.h"

t_vec	vmul(t_vec v, float c)
{
	return ((t_vec){
		.x = v.x * c,
		.y = v.y * c,
		.z = v.z * c,
		.w = v.w * c,
	});
}

t_vec	vmag(t_vec v, t_vec m)
{
	return ((t_vec){
		.x = v.x * m.x,
		.y = v.y * m.y,
		.z = v.z * m.z,
		.w = v.w * m.w,
	});
}
